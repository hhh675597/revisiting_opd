"""
Visualize how a teacher model's per-token entropy changes across the length of
sequences generated by different models.

Pipeline (mirrors infer.py):
  1. Load a math problem from the dataset.
  2. Apply the environment-specific prompt (MATH_TEMPLATE).
  3. Generate a response with a *generator* model via vLLM.
  4. Compute the teacher model's per-position entropy over the full
     (prompt + response) sequence using HuggingFace for exact logits.
  5. Plot entropy vs. position with milestone markers (4k, 8k, 12k, 16k).

Two stages:
  Stage A - generator = Math Teacher  (OpenThinker3-7B)
  Stage B - generator = Student       (qwen-2.5-7b-it)
In both stages the *evaluator* is always the Math Teacher.
"""

