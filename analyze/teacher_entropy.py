def main():
    parser = argparse.ArgumentParser(description="Teacher entropy vs. sequence position (multi-GPU)")
    parser.add_argument("--teacher", default=MATH_TEACHER, help="Path to math teacher model")
    parser.add_argument("--student", default=STUDENT_MODEL1, help="Path to student model")
    parser.add_argument("--student2", default=STUDENT_MODEL2, help="Path to student model 2")
    parser.add_argument("--data", default=DATA_PATH, help="Path to math test parquet")
    parser.add_argument("--num_samples", type=int, default=8, help="Number of samples (one per GPU)")
    parser.add_argument("--start_idx", type=int, default=0, help="Starting problem index")
    parser.add_argument("--random_sample", action="store_true", help="Randomly sample problems")
    parser.add_argument("--max_new_tokens", type=int, default=MAX_NEW_TOKENS)
    parser.add_argument("--out_dir", default="analyze/outputs", help="Directory for plots and data")
    parser.add_argument("--gpu_mem", type=float, default=0.85, help="vLLM GPU memory utilization")
    parser.add_argument("--num_gpus", type=int, default=NUM_GPUS, help="Number of GPUs to use")
    parser.add_argument("--stages", nargs="+", default=["teacher", "student", "student2"],
                        choices=["teacher", "student", "student2"],
                        help="Which stages to run")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for sampling")
    args = parser.parse_args()

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    print("Loading dataset …")
    ds = datasets.load_dataset("parquet", data_files=args.data, split="train")
    print(f"Dataset size: {len(ds)}")

    if args.random_sample:
        random.seed(args.seed)
        problem_indices = random.sample(range(len(ds)), min(args.num_samples, len(ds)))
    else:
        problem_indices = list(range(args.start_idx, min(args.start_idx + args.num_samples, len(ds))))

    print(f"Selected problems: {problem_indices}")

    all_results = {}

    # --- Stage A: Teacher generates, teacher evaluates ---
    if "teacher" in args.stages:
        avg_ent, std_ent, valid_counts, meta = run_stage_multi_gpu(
            stage_name=NAME["teacher"],
            generator_path=args.teacher,
            teacher_path=args.teacher,
            dataset=ds,
            problem_indices=problem_indices,
            max_new_tokens=args.max_new_tokens,
            gpu_memory_utilization=args.gpu_mem,
            num_gpus=args.num_gpus,
        )
        all_results["teacher"] = (avg_ent, std_ent, valid_counts, meta)

        if meta["valid_count"] > 0:
            plot_path = out_dir / f"entropy_teacher_avg_n{meta['valid_count']}.png"
            plot_entropy_averaged(
                avg_ent, std_ent, valid_counts,
                title=f"Teacher Entropy - {NAME['teacher']} (n={meta['valid_count']})",
                save_path=str(plot_path),
            )

    # --- Stage B: Student generates, teacher evaluates ---
    if "student" in args.stages:
        avg_ent, std_ent, valid_counts, meta = run_stage_multi_gpu(
            stage_name=NAME["student"],
            generator_path=args.student,
            teacher_path=args.teacher,
            dataset=ds,
            problem_indices=problem_indices,
            max_new_tokens=args.max_new_tokens,
            gpu_memory_utilization=args.gpu_mem,
            num_gpus=args.num_gpus,
        )
        all_results["student"] = (avg_ent, std_ent, valid_counts, meta)

        if meta["valid_count"] > 0:
            plot_path = out_dir / f"entropy_student_avg_n{meta['valid_count']}.png"
            plot_entropy_averaged(
                avg_ent, std_ent, valid_counts,
                title=f"Teacher Entropy - {NAME['student']} (n={meta['valid_count']})",
                save_path=str(plot_path),
            )

    # --- Stage C: Student2 generates, teacher evaluates ---
    if "student2" in args.stages:
        avg_ent, std_ent, valid_counts, meta = run_stage_multi_gpu(
            stage_name=NAME["student2"],
            generator_path=args.student2,
            teacher_path=args.teacher,
            dataset=ds,
            problem_indices=problem_indices,
            max_new_tokens=args.max_new_tokens,
            gpu_memory_utilization=args.gpu_mem,
            num_gpus=args.num_gpus,
        )
        all_results["student2"] = (avg_ent, std_ent, valid_counts, meta)

        if meta["valid_count"] > 0:
            plot_path = out_dir / f"entropy_student2_avg_n{meta['valid_count']}.png"
            plot_entropy_averaged(
                avg_ent, std_ent, valid_counts,
                title=f"Teacher Entropy - {NAME['student2']} (n={meta['valid_count']})",
                save_path=str(plot_path),
            )

    # --- Combined comparison plot ---
    valid_results = {k: v for k, v in all_results.items() if v[3]["valid_count"] > 0}
    if len(valid_results) >= 2:
        print("\nGenerating combined comparison plot …")
        fig, ax = plt.subplots(figsize=(16, 6))
        window = 64
        kernel = np.ones(window) / window
        palette = {"teacher": "red", "student": "green", "student2": "blue"}

        max_len = 0
        for label, (avg_ent, std_ent, valid_counts, meta) in valid_results.items():
            n = len(avg_ent)
            if n == 0:
                continue
            max_len = max(max_len, n)
            positions = np.arange(n)
            smoothed = np.convolve(avg_ent, kernel, mode="same") if n > window else avg_ent
            smoothed_std = np.convolve(std_ent, kernel, mode="same") if n > window else std_ent

            ax.fill_between(positions, smoothed - smoothed_std, smoothed + smoothed_std,
                            alpha=0.15, color=palette[label])
            ax.plot(positions, smoothed, linewidth=1.2, color=palette[label],
                    label=f"{NAME[label]} (mean={np.nanmean(avg_ent):.2f})")

        ax.set_xlabel("Position in Generated Response", fontweight="bold")
        ax.set_ylabel("Teacher Entropy", fontweight="bold")
        # ax.set_title(f"Teacher Entropy Compariso", fontweight="bold")
        ax.legend(loc="upper right", fontsize=8)
        ax.set_xlim(0, max_len)
        ax.grid(True)
        fig.tight_layout()

        cmp_path_png = out_dir / f"entropy_comparison_avg_n{args.num_samples}.png"
        cmp_path_pdf = out_dir / f"entropy_comparison_avg_n{args.num_samples}.pdf"
        fig.savefig(str(cmp_path_png), dpi=150)
        fig.savefig(str(cmp_path_pdf), dpi=150)
        plt.close(fig)
        print(f"  Saved comparison -> {cmp_path_png}")

    # Save metadata
    meta_path = out_dir / f"entropy_metadata_n{args.num_samples}.json"
    meta_to_save = {
        label: {
            "valid_count": meta["valid_count"],
            "problem_indices": meta["problem_indices"],
            "response_lens": meta["response_lens"],
            "max_len": meta["max_len"],
            "avg_entropy_mean": meta["avg_entropy_mean"],
        }
        for label, (_, _, _, meta) in all_results.items()
    }
    with open(meta_path, "w") as f:
        json.dump(meta_to_save, f, indent=2)
    print(f"  Saved metadata -> {meta_path}")

    print("\nDone.")


if __name__ == "__main__":
    main()
