#!/usr/bin/env python3
"""
Visualize how a teacher model's per-token entropy changes across the length of
sequences generated by different models.

Multi-GPU version:
  - Generation: vLLM handles all prompts in one batch (vLLM does its own
    GPU scheduling, no need for manual multi-GPU).
  - Entropy: 8 GPUs in parallel, each evaluating one sample via a subprocess
    that sets CUDA_VISIBLE_DEVICES *before* importing torch.

Pipeline (mirrors infer.py):
  1. Load math problems from the dataset (one per GPU).
  2. Apply the environment-specific prompt (MATH_TEMPLATE).
  3. Generate all responses with a *generator* model via vLLM (batched).
  4. Compute the teacher model's per-position entropy in parallel (1 GPU per sample).
  5. Average entropy across samples and plot.

Two stages:
  Stage A - generator = Math Teacher  (OpenThinker3-7B)
  Stage B - generator = Student       (qwen-2.5-7b-it)
In both stages the *evaluator* is always the Math Teacher.
"""

